const dictionaries: Record<LanguageMode, Record<TranslationKey, string>> = {
  en,
  zh
};

export const languageOptions: Array<{ value: LanguageMode; labelKey: TranslationKey }> = [
  { value: "en", labelKey: "language.en" },
  { value: "zh", labelKey: "language.zh" }
];

export function translate(language: LanguageMode, key: TranslationKey, values?: Record<string, string | number>) {
  const template = dictionaries[language][key] ?? dictionaries.en[key] ?? key;
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (match, name) => String(values[name] ?? match));
}

export function useI18n() {
  const language = useSettingsStore((state) => state.language);
  const setLanguage = useSettingsStore((state) => state.setLanguage);
  const t = useCallback(
    (key: TranslationKey, values?: Record<string, string | number>) => translate(language, key, values),
    [language]
  );

  return {
    language,
    setLanguage,
    locale: language === "zh" ? "zh-CN" : "en",
    t
  };
}
